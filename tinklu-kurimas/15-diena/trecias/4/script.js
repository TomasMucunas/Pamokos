// Определяем функцию mean
function mean(arr) {
    // Проверяем, что массив не пустой
    if (arr.length === 0) {
        return 0; // Возвращаем 0 для пустого массива
    }

    // Находим сумму всех элементов массива
    let sum = 0; // Инициализируем переменную для хранения суммы

    for (let i = 0; i < arr.length; i++) {
        sum += arr[i]; // Добавляем каждый элемент к сумме
    }

    // Возвращаем среднее значение
    return sum / arr.length; // Делим сумму на количество элементов
}

// Примеры использования функции
console.log(mean([30, 10, 20])); // Вывод: 20
console.log(mean([-10, 10])); // Вывод: 0
console.log(mean([5, 5, 5, 5])); // Вывод: 5
console.log(mean([])); // Вывод: 0